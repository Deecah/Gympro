package model;

public class ExerciseProgram {
    private int exerciseProgramID;
    private int exerciseLibraryID;
    private int trainerID;
    private int programID;

    public int getExerciseProgramID() {
        return exerciseProgramID;
    }

    public void setExerciseProgramID(int exerciseProgramID) {
        this.exerciseProgramID = exerciseProgramID;
    }

    public int getExerciseLibraryID() {
        return exerciseLibraryID;
    }

    public void setExerciseLibraryID(int exerciseLibraryID) {
        this.exerciseLibraryID = exerciseLibraryID;
    }

    public int getTrainerID() {
        return trainerID;
    }

    public void setTrainerID(int trainerID) {
        this.trainerID = trainerID;
    }

    public int getProgramID() {
        return programID;
    }

    public void setProgramID(int programID) {
        this.programID = programID;
    }
}
